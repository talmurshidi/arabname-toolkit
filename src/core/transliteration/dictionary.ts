export interface DictionaryCandidate {
  words: string[];
  arabic: string;
}

function buildIndex(dictionary: Record<string, string>): Map<string, DictionaryCandidate[]> {
  const index = new Map<string, DictionaryCandidate[]>();
  for (const [latin, arabic] of Object.entries(dictionary)) {
    const words = latin.split(/\s+/).filter(Boolean);
    if (words.length === 0) continue;
    const firstWord = (words[0] ?? '').toLowerCase();
    const candidates = index.get(firstWord) ?? [];
    candidates.push({ words, arabic });
    index.set(firstWord, candidates);
  }
  for (const candidates of index.values()) {
    candidates.sort((a, b) => b.words.length - a.words.length);
  }
  return index;
}

export const DICTIONARY_BY_FIRST_WORD: Map<string, DictionaryCandidate[]> = buildIndex(
  TRANSLITERATION_DICTIONARY
);
